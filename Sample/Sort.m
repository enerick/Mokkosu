#------------------------------------------------------------
# Quick sort
#------------------------------------------------------------
fun sort lis =
  match lis {
    [] -> [];
    [x] -> [x];
    x::xs ->
      let (ls,gs) = (filter {i->i<x} xs, filter {i->x<i} xs) in
      sort ls ++ [x] ++ sort gs;
  };

#------------------------------------------------------------
# Test
#------------------------------------------------------------
